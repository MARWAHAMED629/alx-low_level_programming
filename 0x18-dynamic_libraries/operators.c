#include "stdio.h"

/**
 * add - it is add two integer.
 * @x:iirst int .
 * @i: second int.
 * Return - Addition
 */

int add(int x, int i)
{
return (x + i);
}


/**
 * mod - mod two integer.
 * @x:fiirst int .
 * @i: second int.
 * Return - final result.
 */

int mod(int x, int i)
{
return (x % i);
}


/**
 * sub - it is subtract two integer.
 * @x:iirst int .
 * @i: second int.
 * Return - Subtract the final Result.
 */

int sub(int x, int i)
{
return (x - i);
}


/**
 * multi - it is multiply two integer.
 * @x:first int .
 * @i: second int.
 * Return - result multiply
 */

int multi(int x, int i)
{
return (x * i);
}

/**
 * division - it is divided two integer.
 * @x: first int .
 * @i: second int.
 * Return - division result .
 */

int division(int x, int i)
{
return (x / i);
}
